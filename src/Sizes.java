public class Sizes {
    private int id;
    private int swedishSize;

    public Sizes(int id, int swedishSize) {
        this.id = id;
        this.swedishSize = swedishSize;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSwedishSize() {
        return swedishSize;
    }

    public void setSwedishSize(int swedishSize) {
        this.swedishSize = swedishSize;
    }
}
